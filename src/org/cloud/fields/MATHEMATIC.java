public class MATHEMATIC {

    public Double SUM(Double a,Double b)
    {
        return a+b;
    }
    public Double MINUS(Double a,Double b)
    {
        return a-b;
    }
    public Double AREA(Double a,Double b)
    {
        return a*b;
    }
    public Double CONCAT(Double a,Double b)
    {
        return a*b;
    }
public Double Cube(Double a)
    {
        return Math.pow(a,3);
    }}